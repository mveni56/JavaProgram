package setExample;
import java.util.TreeSet;

public class TreeSetExp {
    public void main(String[] args)
    {
        TreeSet<String> fruits=new TreeSet<String>();
        fruits.add("Apple");
        fruits.add("Banana");
        fruits.add("Orange");
        fruits.add("Mango");
        fruits.add("Grapes");
        System.out.println("\nTreeSet: " + fruits);
    }
}
